package practiceOops;

public class EvenNumber {
    //Print only even numbers between 1 and 50 using a for loop.
    public static void main(String[] args) {

        for (int i = 2; i <= 50; i++) {


            if (i % 2 == 0) {
                System.out.println(i);
            }


        }

    }
}
