package TodayPractice30;

public class StarPrint123 {
    public static void main(String[]args){
        int row=4;
        int colum=5;
        for(int i=1;i<=row;i++){
            for(int j=1;j<=colum;j++){
                System.out.print("*");

            }
            System.out.println();
        }
    }
}
